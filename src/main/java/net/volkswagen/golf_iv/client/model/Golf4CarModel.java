    /**
     * Updates model part visibilities dynamically according to the entity's current components.
     *
     * @param plain Whether the vehicle is a plain body template.
     * @param hasSteer Whether a steering wheel is installed.
     * @param wheelsCount The number of wheels installed (0-4).
     * @param lightsCount The number of headlights installed (0-4).
     */
    public void updateComponents(boolean plain, boolean hasSteer, int wheelsCount, int lightsCount) {
        if (plain) {
            this.steer.visible = false;
            this.wheels.visible = false;
            this.lights.visible = false;
        } else {
            this.steer.visible = hasSteer;
            this.wheels.visible = true;
            this.left.visible = wheelsCount >= 1;
            this.right.visible = wheelsCount >= 2;
            this.left2.visible = wheelsCount >= 3;
            this.right2.visible = wheelsCount >= 4;

            this.lights.visible = true;
            this.front2.visible = lightsCount >= 2;
            this.back2.visible = lightsCount >= 4;
        }
    }

    /**
     * Sets the rotation of the wheels.
     *
     * @param rotationRadians The rotation angle in radians.
     */
    public void setWheelRotation(float rotationRadians) {
        this.left.xRot = rotationRadians;
        this.right.xRot = rotationRadians;
        this.left2.xRot = rotationRadians;
        this.right2.xRot = rotationRadians;
    }

